package uk.ashleybye.tictactoe.game;

public class GameOptions {

  private String playerOneName;
  private String playerTwoName;
  private String playerOneType;
  private String playerTwoType;
  private String playerOneSymbol;
  private String playerTwoSymbol;
  private int firstPlayer;

  public String getPlayerOneName() {
    return playerOneName;
  }

  public void setPlayerOneName(String playerOneName) {
    this.playerOneName = playerOneName;
  }

  public String getPlayerTwoName() {
    return playerTwoName;
  }

  public void setPlayerTwoName(String playerTwoName) {
    this.playerTwoName = playerTwoName;
  }

  public String getPlayerOneType() {
    return playerOneType;
  }

  public void setPlayerOneType(String playerOneType) {
    this.playerOneType = playerOneType;
  }

  public String getPlayerTwoType() {
    return playerTwoType;
  }

  public void setPlayerTwoType(String playerTwoType) {
    this.playerTwoType = playerTwoType;
  }

  public String getPlayerOneSymbol() {
    return playerOneSymbol;
  }

  public void setPlayerOneSymbol(String playerOneSymbol) {
    this.playerOneSymbol = playerOneSymbol;
  }

  public String getPlayerTwoSymbol() {
    return playerTwoSymbol;
  }

  public void setPlayerTwoSymbol(String playerTwoSymbol) {
    this.playerTwoSymbol = playerTwoSymbol;
  }

  public int getFirstPlayer() {
    return firstPlayer;
  }

  public void setFirstPlayer(int firstPlayer) {
    this.firstPlayer = firstPlayer;
  }
}
